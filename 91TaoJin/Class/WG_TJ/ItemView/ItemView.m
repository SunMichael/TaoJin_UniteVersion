#import "ItemView.h"

@implementation ItemView

- (id)initWithFrame:(CGRect)frame withImage:(UIImage* )image andWithString:(NSString* )string
{
    self = [super initWithFrame:frame];
    if (self) {
       imageVeiw.frame=CGRectMake(37, 27, klogowidth, klogoheigh);
        imageVeiw.image=image;
       label.frame=CGRectMake(37, 72, klabelWidth, klabelHeigh);
        label.text=string;
        [self addSubview:imageVeiw];
        [self addSubview:label];
    }
    return self;
}



@end
