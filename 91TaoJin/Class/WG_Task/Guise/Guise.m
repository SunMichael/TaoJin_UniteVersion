#import "Guise.h"

@implementation Guise

/**
 *  初始化伪装对象
 *
 *  @param iconImg        图标
 *  @param titleStr       标题
 *  @param subTitleStr    子标题
 *  @param subContentStr  内容
 *  @param jindouCountStr 金豆数
 *
 */
-(id)initWithImage:(UIImage *)iconImg titleStr:(NSString *)titleStr subTitleStr:(NSString *)subTitleStr subContentStr:(NSString *)subContentStr jindouCountStr:(NSString *)jindouCountStr urlStr:(NSString *)urlStr btnStr:(NSString *)btnStr{
    self = [super init];
    if(self){
        self.guise_iconImg = iconImg;
        self.guise_titleStr = titleStr;
        self.guise_subTitleStr = subTitleStr;
        self.guise_subContentStr = subContentStr;
        self.guise_jindouCountStr = jindouCountStr;
        self.guise_url = urlStr;
        self.guise_buttonStr = btnStr;
    }
    return self;
}

@end
